function getRepeatableQuestTier(player)
  for i = 1, #repeatableQuests do
    local tier = repeatableQuests[i]

    if player.level >= tier.minLevel
      and player.level <= tier.maxLevel
      and player.karma < tier.maxKarma
    then
      return tier
    end
  end

  return nil
end

RepeatableQuestNpc = {
  click = async(function(player, npc)
		local t = {
			graphic = convertGraphic(npc.look, "monster"),
			color = npc.lookColor
		}
		player.npcGraphic = t.graphic
		player.npcColor = t.color
		player.dialogType = 0
		player.lastClick = npc.ID
    
    local tier = getRepeatableQuestTier(player)

    if tier == nil then
      player:dialogSeq({
        t,
        "You are not eligible for repeatable quests."
      }, 0)
      return
    end

    local options = {}

    for i = 1, #tier.quests do
      local quest = tier.quests[i]
      local label = quest.amount .. " pcs " .. quest.itemName

      table.insert(options, label)
    end

    local choice = player:menuString(
      "Which quest do you want to take?",
      options
    )

    local selectedQuest

    for i = 1, #tier.quests do
      local quest = tier.quests[i]

      if choice == (quest.amount .. " pcs " .. quest.itemName) then
        selectedQuest = quest
        break
      end
    end

    if selectedQuest == nil then
      player:dialogSeq({
        t,
        "Invalid quest selection."
      }, 0)

      return
    end

    if player:hasItem(
      selectedQuest.itemIdentifier,
      selectedQuest.amount
    ) ~= true then
      player:dialogSeq({
        t,
        "You don't have enough items."
      }, 0)

      return
    end

    player:removeItem(
      selectedQuest.itemIdentifier,
      selectedQuest.amount
    )

    player:addGold(selectedQuest.gold)
    KarmaSystem.addKarma(player, selectedQuest.karma)	
    player:forceSave()

    player:dialogSeq({
      t,
      "Excellent work!",
      "Reward:\n" ..
      selectedQuest.gold ..
      " gold\n" ..
      string.format("%.5f", selectedQuest.karma) ..
      " karma"
    }, 1)

  end),
}